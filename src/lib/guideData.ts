// localStorage 키
const GUIDE_STORAGE_KEY = "maumteo_guides_seen";

export function getSeenGuides(): Set<string> {
  try {
    const raw = localStorage.getItem(GUIDE_STORAGE_KEY);
    if (raw) return new Set(JSON.parse(raw));
  } catch { /* ignore */ }
  return new Set();
}

export function markGuideSeen(id: string): void {
  const seen = getSeenGuides();
  seen.add(id);
  try {
    localStorage.setItem(GUIDE_STORAGE_KEY, JSON.stringify([...seen]));
  } catch { /* ignore */ }
}

export function resetSeenGuides(): void {
  try {
    localStorage.removeItem(GUIDE_STORAGE_KEY);
  } catch { /* ignore */ }
}
